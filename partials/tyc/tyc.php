VII. USO DE COOKIES

El Sitio Web utiliza Cookies propias y de terceros con la finalidad de: (i) optimizar los
servicios ofrecidos para los Usuarios, (ii) ofrecer una mejor experiencia de navegación al
Usuario, y (iii) brindarle información de todo tipo al Usuario conforme con sus preferencias.

Estas cookies pueden ser de varios tipos:

1. Por su duración: (i) cookie de sesión, esto es, aquellas que únicamente
permanecen activas mientras el Usuario registrado haga uso de un determinado
espacio web, permitiendo identificar al mismo con el fin de personalizar su
experiencia. Finalizada la sesión, esta cookie desaparecerá sin que sean
almacenadas en el disco duro del dispositivo, (ii) cookie permanente, esto es,
aquella programada directamente por el

Sitio Web con el fin de realizar el seguimiento al Usuario cada vez que se genera
una nueva visita.

2. Por su finalidad: (i) cookies esenciales o técnicas, esto es, aquellas indispensables
para garantizar la adecuada navegación, permitiendo el correcto tráfico y
reconocimiento de datos, (ii) cookies de personalización, aquellas que almacenan
las preferencias de cierto dispositivo como por ejemplo, el idioma, el tipo de
navegador, las características que tiene el portal dependiendo el lugar del mundo
desde el que se acceda a él, etc., (iii) cookies de análisis, aquellas que permiten la
creación de un perfil de navegación por Usuario, analizando el comportamiento de
este y sus preferencias, y (iv) cookies publicitarias, esto es, aquellas usadas para
campañas de marketing propias o de terceros, destinadas a dar a conocer al
Usuario productos y/o servicios en espacios previamente definidos por el Usuario
para el efecto. Estas cookies pueden ser de tipo comportamental, es decir, que se
diseñan para almacenar información sobre las visitas frecuentes del Usuario y la
observación continuada de sus hábitos de navegación, esto a su vez, permite crear
un perfil para mostrar publicidad de acuerdo con el análisis realizado.

Por lo tanto, el Usuario acepta que el Sitio Web utilice Cookies para recolectar información
sobre su interacción el mismo. En todo caso, el Usuario puede escoger visitar el Sitio Web
sin Cookies, para lo cual puede su navegador para que rechace todas las Cookies, o para
que le avise cuando se cree una Cookie. (Cada navegador es diferente, de modo que
deberá consultar el menú «Ayuda» del suyo para saber cómo modificar sus preferencias
con relación a las Cookies.)

